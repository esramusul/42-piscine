#include <stdio.h>

int	ft_strlen(char *str)
{
	int	len;

	len = 0;
	while (str[len] != '\0')
	{
		len++;
	}
	return (len);
}

int	main()
{
	char	*c = "hello";
	printf("%d\n", ft_strlen(c));
}
