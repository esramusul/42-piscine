char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	a;

	i = 0;
	a = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[a] != '\0')
	{
		dest[i] = src[a];
		i++;
		a++;
	}
	dest[i] = '\0';
	return (dest);
}

#include<stdio.h>
int main()
{
	char dest[]="esra ";
	char src[]="musul";
	printf("%s",ft_strcat(dest,src));
}
